#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	while (s && *s != '\0' && *s != c)
		s++;
	if (!s || (c != '\0' && *s == '\0'))
		return (NULL);
	else
		return ((char *)s);
}
